using namespace std;

DataOutputStream::DataOutputStream(char * filename) {
  fp = NULL;
  file_was_opened = false;
  if ( filename == NULL) {
    fp = stdout;
    }
  else {
    fp = open_write_file(filename);
    file_was_opened=true;
    }
}

DataOutputStream::~DataOutputStream() {
  if (file_was_opened)
    fclose(fp);
}

